package io.moyada.medivh.support;

/**
 * 语法类型
 * @author xueyikang
 * @since 1.0
 **/
public enum TypeTag {

    BOOLEAN, // 布尔值
    BYTE, // 字节
    SHORT, // 短整形
    INT, // 整形
    LONG, // 长整形
    FLOAT, // 单精度浮点数
    DOUBLE, // 双精度浮点数
    CHAR, // 字符
    NE, // 不等于
    EQ, // 等于
    GT, // 大于
    LT, // 小于
    PLUS, // + 操作
    BOT, // null
    CLASS, // 对象
    ;
}
